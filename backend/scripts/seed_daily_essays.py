async def _import_async():
    from sqlalchemy.ext.asyncio import create_async_engine, async_sessionmaker
    from sqlalchemy import select, func, delete, text

    engine = create_async_engine(settings.DATABASE_URL, echo=False)
    SessionLocal = async_sessionmaker(engine, expire_on_commit=False)

    async with engine.begin() as conn:
        from app.core.database import Base
        await conn.run_sync(Base.metadata.create_all)

    async with SessionLocal() as db:
        # Skip if data exists
        result = await db.execute(select(func.count()).select_from(text("daily_essays")))
        existing = result.scalar()
        if existing > 0:
            print(f"已有 {existing} 篇范文，跳过种子数据")
            return

        from app.models.daily_essay import DailyEssay

        today = date.today()
        for i, essay in enumerate(ESSAYS):
            rec_date = today - timedelta(days=i)
            record = DailyEssay(
                title=essay["title"],
                content=essay["content"],
                topic=essay["topic"],
                source_name=essay["source_name"],
                source_url=essay["source_url"],
                exam_category=essay["exam_category"],
                recommend_date=rec_date,
                highlights=essay.get("highlights", ""),
                key_points=essay.get("key_points", ""),
            )
            db.add(record)

        await db.commit()
        print(f"成功导入 {len(ESSAYS)} 篇范文")

        # Stats by category
        for cat in ["行政执法", "县乡基层", "省市直"]:
            result = await db.execute(
                select(func.count(DailyEssay.id)).where(DailyEssay.exam_category == cat)
            )
            cnt = result.scalar()
            print(f"  {cat}: {cnt} 篇")

    await engine.dispose()


