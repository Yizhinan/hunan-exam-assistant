# Async PostgreSQL import
# ======================================================================
async def _import_async():
    from sqlalchemy.ext.asyncio import create_async_engine, async_sessionmaker
    from sqlalchemy import select, func, delete

    engine = create_async_engine(settings.DATABASE_URL, echo=False)
    SessionLocal = async_sessionmaker(engine, expire_on_commit=False)

    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async with SessionLocal() as db:
        # Count existing
        result = await db.execute(select(func.count(PositionHistory.id)))
        existing = result.scalar()
        if existing > 0:
            print(f"已有 {existing} 条数据，删除后重新导入...")
            await db.execute(delete(PositionHistory))
            await db.commit()
            print("旧数据已清除")

        positions = build_positions()
        for p in positions:
            db.add(PositionHistory(**p))
        await db.commit()
        print(f"成功导入 {len(positions)} 条岗位数据（覆盖 2023-2026 年）")

        # Stats by year
        for year in [2023, 2024, 2025, 2026]:
            result = await db.execute(
                select(func.count(PositionHistory.id)).where(PositionHistory.year == year)
            )
            count = result.scalar()
            print(f"  {year}年: {count} 条")

    await engine.dispose()


