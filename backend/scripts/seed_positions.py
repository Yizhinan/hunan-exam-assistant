def _import_sync():
    from app.core.database import engine, Base, SessionLocal as SyncSessionLocal
    from sqlalchemy import func

    Base.metadata.create_all(bind=engine)
    db = SyncSessionLocal()

    existing = db.query(func.count(PositionHistory.id)).scalar()
